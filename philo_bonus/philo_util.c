#include "philo_bonus.h"

int64_t	ft_atol(const char *nptr)
{
	int64_t	n;
	int64_t	sign;

	n = 0;
	sign = 1;
	while (*nptr == ' ' || *nptr == '\f' || *nptr == '\n'
		|| *nptr == '\r' || *nptr == '\t' || *nptr == '\v')
		nptr++;
	if (*nptr == '-')
	{
		sign = -sign;
		nptr++;
	}
	else if (*nptr == '+')
		nptr++;
	while (*nptr >= '0' && *nptr <= '9')
	{
		n = 10 * n + (*nptr - '0');
		nptr++;
	}
	return (n * sign);
}

int64_t	get_time_ms(void)
{
	struct timeval	tv;
	int64_t			time_in_mill;

	gettimeofday(&tv, NULL);
	time_in_mill = (tv.tv_sec) * 1000 + (tv.tv_usec) / 1000;
	return (time_in_mill);
}

void	ft_usleep(int ms)
{
	long	time;

	time = get_time_ms();
	usleep(ms * 920);
	while (get_time_ms() < time + (long)ms)
		usleep(ms * 3);
}
